    // Crear las tablas en la base de datos
	public static void crearTablas() {
	    try (Connection conn = Singleton.getInstance().getConnection();
	         Statement stmt = conn.createStatement()) {

	    	   stmt.executeUpdate("""
	   	            CREATE TABLE IF NOT EXISTS usuarios (
	   	                idUsuario INTEGER PRIMARY KEY AUTOINCREMENT,
	   	                nombre TEXT NOT NULL,
	   	                correo TEXT UNIQUE NOT NULL,
	   	                contrasena TEXT NOT NULL
	   	            );
	   	        """);

	   	        // Expansiones
	   	        stmt.executeUpdate("""
	   	            CREATE TABLE IF NOT EXISTS expansiones (
	   	                id_expansion INTEGER PRIMARY KEY AUTOINCREMENT,
	   	                nombre TEXT NOT NULL,
	   	                codigo TEXT UNIQUE NOT NULL
	   	            );
	   	        """);

	   	        // Cartas
	   	        stmt.executeUpdate("""
	   	            CREATE TABLE IF NOT EXISTS cartas (
	   	                referencia TEXT PRIMARY KEY,
	   	                nombre TEXT NOT NULL,
	   	                id_expansion INTEGER NOT NULL,
	   	                tipo TEXT,
	   	                subtipo TEXT,
	   	                precio REAL,
	   	                referencia_comercial TEXT,
	   	                FOREIGN KEY(id_expansion) REFERENCES expansiones(id_expansion)
	   	            );
	   	        """);

	   	        // Precios hist√≥ricos
	   	        stmt.executeUpdate("""
	           		CREATE TABLE IF NOT EXISTS precios_historicos (
	   				    referencia TEXT NOT NULL,
	   				    fecha TEXT NOT NULL,
	   				    historico TEXT NOT NULL,
	   				    PRIMARY KEY(referencia, fecha),
	   				    FOREIGN KEY(referencia) REFERENCES cartas(referencia)
	   					);
	   		        """);

	   	        // Logs SQL
	   	        stmt.executeUpdate("""
	   	            CREATE TABLE IF NOT EXISTS logs_sql (
	   	                idLog INTEGER PRIMARY KEY AUTOINCREMENT,
	   	                accion TEXT NOT NULL,
	   	                entidad TEXT NOT NULL,
	   	                fecha TEXT NOT NULL,
	   	                duracion_ms INTEGER,
	   	                detalles TEXT
	   	            );
	   	        """);

	   	        // Errores SQL
	   	        stmt.executeUpdate("""
	   	            CREATE TABLE IF NOT EXISTS errores_sql (
	   	                idError INTEGER PRIMARY KEY AUTOINCREMENT,
	   	                error TEXT NOT NULL,
	   	                fecha TEXT NOT NULL,
	   	                clase TEXT,
	   	                metodo TEXT
	   	            );
	   	        """);

	   	        // Wishlist
	   	        stmt.executeUpdate("""
	   	            CREATE TABLE IF NOT EXISTS wishlist (
	   	                idWishlist INTEGER PRIMARY KEY AUTOINCREMENT,
	   	                idUsuario INTEGER NOT NULL,
	   	                nombre TEXT NOT NULL,
	   	                FOREIGN KEY(idUsuario) REFERENCES usuarios(idUsuario)
	   	            );
	   	        """);

	   	        // Wishlist Items
	   	        stmt.executeUpdate("""
					CREATE TABLE IF NOT EXISTS wishlist_items (
					    idWishlistItem INTEGER PRIMARY KEY AUTOINCREMENT,
					    idWishlist INTEGER NOT NULL,
					    referencia TEXT NOT NULL,
					    cantidadDeseada INTEGER DEFAULT 1,
					    precioMinimo REAL DEFAULT 0.0,
					    precioMaximo REAL DEFAULT 0.0,
					    alertaMinimo BOOLEAN DEFAULT FALSE,
					    alertaMaximo BOOLEAN DEFAULT FALSE,
					    gradeada BOOLEAN DEFAULT FALSE,
					    nota INTEGER DEFAULT 0,
					    FOREIGN KEY(idWishlist) REFERENCES wishlist(idWishlist),
					    FOREIGN KEY(referencia) REFERENCES cartas(referencia)
					);

	   	        """);

	   	     // sobres
	   	        stmt.executeUpdate("""
	   	            CREATE TABLE IF NOT EXISTS sobres (
					    idSobre INTEGER PRIMARY KEY AUTOINCREMENT,
					    nombre  TEXT    NOT NULL UNIQUE,
					    url_pricecharting TEXT NOT NULL,
					    precio  REAL
					);
	   	        """);

	   	        // cajas
	   	        stmt.executeUpdate("""
					CREATE TABLE IF NOT EXISTS cajas (
					    idCaja  INTEGER PRIMARY KEY AUTOINCREMENT,
					    nombre  TEXT    NOT NULL UNIQUE,
					    url_pricecharting TEXT NOT NULL,
					    precio  REAL
					);
	   	        """);
	   	        
	   	        //sobrecolecciones
	   	     stmt.executeUpdate("""
						CREATE TABLE IF NOT EXISTS sobres_cajas_usuario (
						    id INTEGER PRIMARY KEY AUTOINCREMENT,
						    idUsuario INTEGER NOT NULL,
						    idElemento INTEGER NOT NULL,
						    nombre TEXT NOT NULL,
						    tipo TEXT NOT NULL CHECK(tipo IN ('sobre', 'caja')),
						    precio DOUBLE,
						    alertaMin DOUBLE,
						    alertaMax DOUBLE,
						    FOREIGN KEY(idUsuario) REFERENCES usuarios(idUsuario)
						);
		   	        """);
	   	        

	   	        // Colecciones
	   	        stmt.executeUpdate("""
	   	            CREATE TABLE IF NOT EXISTS colecciones (
	   	                idColeccion INTEGER PRIMARY KEY AUTOINCREMENT,
	   	                idUsuario INTEGER NOT NULL,
	   	                nombre TEXT NOT NULL,
	   	                FOREIGN KEY(idUsuario) REFERENCES usuarios(idUsuario)
	   	            );
	   	        """);

	   	        // Colecciones-Cartas
	   	        stmt.executeUpdate("""
	   	            CREATE TABLE IF NOT EXISTS colecciones_cartas (
	   	                idColeccion INTEGER NOT NULL,
	   	                referencia TEXT NOT NULL,
	   	                cantidad INTEGER NOT NULL,
	   	                PRIMARY KEY (idColeccion, referencia),
	   	                FOREIGN KEY(idColeccion) REFERENCES colecciones(idColeccion),
	   	                FOREIGN KEY(referencia) REFERENCES cartas(referencia),
	   	                graduacion INTEGER NULL,
	   	                precioPagado REAL NULL,
	   	                alertaMin REAL NULL,
	   	                alertaMax REAL NULL
	   	                
	   	            );
	   	        """);
	   	        
	   	        stmt.executeUpdate("""
					CREATE TABLE IF NOT EXISTS alertas_enviadas (
					    idUsuario INTEGER,
					    referencia TEXT,
					    tipo TEXT, -- 'min' o 'max'
					    PRIMARY KEY (idUsuario, referencia, tipo)
					);
		   	    """);

	        System.out.println("Tablas creadas correctamente.");

	    } catch (Exception e) {
	        System.err.println("Error al crear las tablas: " + e.getMessage());
	    }
	}

